raymondmodelsmanual <- function(V, S, D, Q) {
	D <- D * 0.001
	Q <- Q * 0.001
	
	Fr <- V/((9.81 * D) ^ (0.5))
	
	k1_low <- (V * S) ^ 0.87 * D ^ 0.51 * (5037 - 604)
	k1_high <- (V * S) ^ 0.91 * D ^ 0.57 * (5037 + 604)
	
	k2_low <- (5937 - 606) * (1 - 2.54 - 0.223 * Fr ^ 2) * (V * S) ^ 0.873 * D ^ 0.31
	k2_high <- (5937 + 606) * (1 - 2.54 - 0.223 * Fr ^ 2) * (V * S) ^ 0.907 * D ^ 0.85
	
	k3_low <- (1162 - 192) * S ^ 0.742 * V ^ 0.805 
	k3_high <- (1162 + 192) * S ^ 0.798 * V ^ 0.895 
	
	k4_low <- (V * S) ^ 0.733 * (951.5 - 144)
	k4_high <- (V * S) ^ 0.787 * (951.5 + 144)
	
	k5_low <- V * S * (2841 - 107) + 2.02 - 0.209
	k5_high <- V * S * (2841 + 107) + 2.02 + 0.209
	
	k6_low <- (929 - 141) * (V * S) ^ 0.723 * Q ^ -0.005
	k6_high <- (929 + 141) * (V * S) ^ 0.777 * Q ^ 0.027
	
	k7_low <- (4725 - 445) * (V * S) ^ 0.844 * Q ^ -0.152 * D ^ 0.631
	k7_high <- (4725 + 445) * (V * S) ^ 0.876 * Q ^ -0.128 * D ^ 0.689
	
	c(k1_low, k1_high,
		k2_low, k2_high,
		k3_low, k3_high,
		k4_low, k4_high,
		k5_low, k5_high,
		k6_low, k6_high,
		k7_low, k7_high)
}